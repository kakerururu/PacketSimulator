"""クラスタリング時のレコード評価アクション"""

from enum import Enum, auto


class RecordAction(Enum):
    """メインループでの候補レコード評価後に取るべきアクション"""

    # クラスタに追加して、次のレコードの評価へ進む
    ADD_TO_CLUSTER = auto()

    # 「ありえない移動」または「滞在時間超過」を検出
    # → 前方探索を開始して、到達可能なレコードを探す
    FORWARD_SEARCH = auto()

    # このレコードをスキップ（現在は使用されていない）
    SKIP = auto()
    # Todo これはフラグをtrueにするのかしないのか


class ForwardSearchAction(Enum):
    """前方探索中のレコード評価後に取るべきアクション"""

    # 同じ検出器での滞在継続としてクラスタに追加し、探索を継続
    # （まだ「次の検出器」は見つかっていない）
    ADD_AND_CONTINUE = auto()

    # このレコードをスキップして、探索を継続
    # - 使用済みレコード
    # - ループになる検出器（既に経路に含まれる）
    # - シーケンス番号異常
    # - 到達不可能
    SKIP = auto()

    # 到達可能なレコードを発見！探索終了
    FOUND = auto()
